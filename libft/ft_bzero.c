#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*buff;
	size_t	i;

	buff = s;
	i = 0;
	if (!n || (n <= 0))
		return ;
	while (i < n)
	{
		buff[i] = '\0';
		i++;
	}
}
